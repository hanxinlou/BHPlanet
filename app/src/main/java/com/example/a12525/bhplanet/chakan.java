package com.example.a12525.bhplanet;

public class chakan {
    private int tou;
    private String name;
    private String time;
    private int dianliang;
    private String pneirong;
    public chakan(int tou,String name,String time,int dianliang,String pneirong)
    {
        this.tou=tou;
        this.name=name;
        this.time=time;
        this.dianliang=dianliang;
        this.pneirong=pneirong;
    }

    public int getTou() {
        return tou;
    }

    public String getName() {
        return name;
    }

    public String getTime() {
        return time;
    }

    public int getDianliang() {
        return dianliang;
    }

    public String getPneirong() {
        return pneirong;
    }
}
